mod error;
pub mod types;

pub use error::{Error, Result};

const PLUGIN_NAME: &str = "mcp";

fn make_specta_builder<R: tauri::Runtime>() -> tauri_specta::Builder<R> {
    tauri_specta::Builder::<R>::new()
        .plugin_name(PLUGIN_NAME)
        .commands(tauri_specta::collect_commands![])
        .typ::<types::CreateIssueParams>()
        .typ::<types::CreateIssueOutput>()
        .typ::<types::AddCommentParams>()
        .typ::<types::AddCommentOutput>()
        .typ::<types::SearchIssuesParams>()
        .typ::<types::SearchIssuesOutput>()
        .typ::<types::SearchIssueItem>()
        .typ::<types::ListSubscriptionsParams>()
        .typ::<types::SubscriptionItem>()
        .typ::<types::CreateBillingPortalSessionParams>()
        .typ::<types::CreateBillingPortalSessionOutput>()
        .error_handling(tauri_specta::ErrorHandlingMode::Result)
}

pub fn init<R: tauri::Runtime>() -> tauri::plugin::TauriPlugin<R> {
    let specta_builder = make_specta_builder();

    tauri::plugin::Builder::new(PLUGIN_NAME)
        .invoke_handler(specta_builder.invoke_handler())
        .build()
}

#[cfg(test)]
mod test {
    use super::*;

    #[test]
    fn export_types() {
        const OUTPUT_FILE: &str = "./js/bindings.gen.ts";

        make_specta_builder::<tauri::Wry>()
            .export(
                specta_typescript::Typescript::default()
                    .formatter(specta_typescript::formatter::prettier)
                    .bigint(specta_typescript::BigIntExportBehavior::Number),
                OUTPUT_FILE,
            )
            .unwrap();

        let content = std::fs::read_to_string(OUTPUT_FILE).unwrap();
        std::fs::write(OUTPUT_FILE, format!("// @ts-nocheck\n{content}")).unwrap();
    }
}
